# Datatype

number = 60  # Int
num = 45.36  # Float
greeting = "Hello there"   # str
isPythonInteresting = True  # boolean
fruits = ["apple", "banana", "cherry"] # List
cars = ("bmw", "Audi", "toyota") # Tuple
countries = {"kenya" ,"Tanzania" , "Uganda"} # Set
student = {
    "first_name": "John",
    "age": 19,
    "course": "MIT",
    "nationality": "Kenyan",
} # Dictionary

print(num)
print(isPythonInteresting)
print(fruits)
print(cars)
print(countries)
print(student["first_name"])

# Determining type of data
print(type(isPythonInteresting))
print(type(cars))

# Typecasting is converting one data type to another
print(int(num))
print(float(number))

