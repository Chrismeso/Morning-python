# Arithmetic operators
num1 = 10
num2 = 3
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # Modulus- Returns the remainder after division

# Comparisons opertaors- Used to compare values
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 <= num2)
print(num1 == num2) # Equal to
print(num1 != num2) # Not equal to

# Assignment operators - Assign values to variables
x = 100
print(x)

x += 2
print(x)

y = 56
y -= 3
print(y)

# Logic operators - and,or,not
a=23
print(a < 40 and a < 2)
print(a < 40 or a < 2)
print(not(a < 40 or a < 2))

# Operators precedence
result = (10+6/2-4)
print(result)

print(int(result))